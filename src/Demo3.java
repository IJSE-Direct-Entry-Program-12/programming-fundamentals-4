void main(){
    int myInt = 10;
    double myDouble = 1250.25;
    String myStr = "IJSE";

    System.out.printf("INT FORMATTING: %20d \n", myInt);
    System.out.printf("DOUBLE FORMATTING: %30.4f \n", myDouble);
    System.out.printf("STRING FORMATTING: %10s \n", myStr);
    System.out.printf("STRING FORMATTING: %40s \n", myInt);
    System.out.printf("STRING FORMATTING: %50s \n", myDouble);
}