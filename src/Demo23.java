void main(){
    int x;
    x = 10;             // Identity Conversion

    double y;
    y = 52.2;           // Identity Conversion

    float f;
    f = 25.2f;          // Identity Conversion

    String str;
    str = "Hello";      // Identity Conversion

    char c = 'c';       // Identity Conversion

    long l = 10l;       // Identity Conversion

    double d = 10;      // Not an identity conversion
    float f2 = 15;      // Not an identity conversion
    byte b = 15;        // Not an identity conversion

    str = null;         // Not an identity conversion

    c = 75;             // Not an identity conversion
}